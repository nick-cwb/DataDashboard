public class FansTarget {
	
	private String newFans;//新增粉丝
	private String netIncreaseFans;//净增粉丝
	private String reducedFans;//取关粉丝
	private String countFans;//累计粉丝数
	private String activeFans;//活跃粉丝
	private String avgActivity;//平均活跃度
	private String date;//日期
	
	/**
	 * @return the newFans
	 */
	public String getNewFans() {
		return newFans;
	}
	/**
	 * @param newFans the newFans to set
	 */
	public void setNewFans(String newFans) {
		this.newFans = newFans;
	}
	/**
	 * @return the netIncreaseFans
	 */
	public String getNetIncreaseFans() {
		return netIncreaseFans;
	}
	/**
	 * @param netIncreaseFans the netIncreaseFans to set
	 */
	public void setNetIncreaseFans(String netIncreaseFans) {
		this.netIncreaseFans = netIncreaseFans;
	}
	/**
	 * @return the reducedFans
	 */
	public String getReducedFans() {
		return reducedFans;
	}
	/**
	 * @param reducedFans the reducedFans to set
	 */
	public void setReducedFans(String reducedFans) {
		this.reducedFans = reducedFans;
	}
	/**
	 * @return the countFans
	 */
	public String getCountFans() {
		return countFans;
	}
	/**
	 * @param countFans the countFans to set
	 */
	public void setCountFans(String countFans) {
		this.countFans = countFans;
	}
	/**
	 * @return the activeFans
	 */
	public String getActiveFans() {
		return activeFans;
	}
	/**
	 * @param activeFans the activeFans to set
	 */
	public void setActiveFans(String activeFans) {
		this.activeFans = activeFans;
	}
	/**
	 * @return the avgActivity
	 */
	public String getAvgActivity() {
		return avgActivity;
	}
	/**
	 * @param avgActivity the avgActivity to set
	 */
	public void setAvgActivity(String avgActivity) {
		this.avgActivity = avgActivity;
	}
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	
	

}
